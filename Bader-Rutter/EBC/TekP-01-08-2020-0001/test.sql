    @SystemID char(12)
AS
SET NOCOUNT ON;
/**********************************************************************************
Client Name: Bader-Rutter

Created By: James Bender
Business Analyst: Cheryl Petitti
Create Date: 01/21/2020
Service Request Number: TekP-01-08-2020-0001

Purpose: EBC Cobra QB Export

Revision History
