        ,drvShipToCountry = EepAddressCountry
        ,drvWebUserName = CONCAT('NCS', EecEmpNo)
        ,drvWebPassword = CONCAT('NCS', EecEmpNo)
        ,drvWebAccessLevel = CASE WHEN JbcJobCode = 'HR020800' AND EecEmplStatus <> 'T' THEN '3' ELSE '1' END
    INTO dbo.U_EARAMRKEXP_drvTbl
    FROM dbo.U_EARAMRKEXP_EEList WITH (NOLOCK)
    JOIN dbo.EmpPers WITH (NOLOCK)
        ON EepEEID = xEEID
    JOIN dbo.vw_int_EmpComp WITH (NOLOCK)
        ON EecEEID = xEEID 
        AND EecCoID = xCoID
    JOIN dbo.JobCode WITH (NOLOCK)
