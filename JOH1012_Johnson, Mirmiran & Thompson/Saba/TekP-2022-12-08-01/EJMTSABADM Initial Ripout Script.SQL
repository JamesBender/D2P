            SELECT DISTINCT OrgCode as OrgCode12 , OrgDesc AS OrgDesc12 , OrgGLSegment as OrgGLSegment2
            FROM dbo.OrgLevel WITH (NOLOCK)
            WHERE OrgLvl = 2
