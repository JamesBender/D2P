        AND PJH.EjhJobCode = EecJobCode
    JOIN (
            SELECT DISTINCT OrgCode as OrgCode11, OrgDesc AS OrgDesc11, OrgGLSegment as OrgGLSegment1
            FROM dbo.OrgLevel WITH (NOLOCK)
            WHERE OrgLvl = 1
            ) AS Org3
