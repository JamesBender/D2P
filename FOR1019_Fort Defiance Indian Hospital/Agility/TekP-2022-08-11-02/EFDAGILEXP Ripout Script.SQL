CREATE PROCEDURE [dbo].[dsi_sp_BuildDriverTables_EFDAGILEXP]
    @SystemID char(12)
AS
SET NOCOUNT ON;
/**********************************************************************************
Client Name: Fort Defiance 

Created By: Marie Waters
Business Analyst: Kim Ephraim
Create Date: 11/29/2022
Service Request Number: TekP-2022-08-11-02

Purpose: Agility Employee Demo Export

Revision History
----------------
Update By           Date           Request Num        Desc
XXXX                XX/XX/2022     SR-2022-000XXXXX   XXXXX

SELECT * FROM dbo.U_dsi_Configuration WHERE FormatCode = 'EFDAGILEXP';
SELECT * FROM dbo.U_dsi_SqlClauses WHERE FormatCode = 'EFDAGILEXP';
SELECT * FROM dbo.U_dsi_Parameters WHERE FormatCode = 'EFDAGILEXP';
SELECT ExpFormatCode, ExpExportCode, ExpStartPerControl, ExpEndPerControl,* FROM dbo.AscExp WHERE expFormatCode = 'EFDAGILEXP';
SELECT * FROM dbo.U_dsi_InterfaceActivityLog WHERE FormatCode = 'EFDAGILEXP' ORDER BY RunID DESC;

Execute Export
--------------
EXEC dbo.dsi_sp_TestSwitchbox_v2 'EFDAGILEXP', 'ONDEM_XOE';
EXEC dbo.dsi_sp_TestSwitchbox_v2 'EFDAGILEXP', 'TEST_XOE';
EXEC dbo.dsi_sp_TestSwitchbox_v2 'EFDAGILEXP', 'SCH_EFDAGI';

EXEC dbo._dsi_usp_ExportRipOut_v7_4 @FormatCode = 'EFDAGILEXP', @AllObjects = 'Y', @IsWeb = 'Y'
**********************************************************************************/
BEGIN

    --==========================================
    -- Declare variables
    --==========================================
    DECLARE  @FormatCode        VARCHAR(10)
            ,@ExportCode        VARCHAR(10)
            ,@StartDate         DATETIME
            ,@EndDate           DATETIME
            ,@StartPerControl   VARCHAR(9)
            ,@EndPerControl     VARCHAR(9);

    -- Set FormatCode
    SELECT @FormatCode = 'EFDAGILEXP';

    -- Declare dates from Parameter file
    SELECT
         @StartPerControl = StartPerControl
        ,@EndPerControl   = EndPerControl
        ,@StartDate       = LEFT(StartPerControl,8)
        ,@EndDate         = DATEADD(S,-1,DATEADD(D,1,LEFT(EndPerControl,8)))
        ,@ExportCode      = ExportCode
    FROM dbo.U_dsi_Parameters WITH (NOLOCK)
    WHERE FormatCode = @FormatCode;

    --==========================================
    -- Clean EE List 
    -- Caution: Careful of cleaning EE List if including paycheck data
    --==========================================

    -- Cleans EE List of terms where EE active in another company (transfer), or active in more than one company
    DELETE FROM dbo.U_EFDAGILEXP_EEList
    WHERE xCoID <> dbo.dsi_BDM_fn_GetCurrentCOID(xEEID)
    AND xEEID IN (SELECT xEEID FROM dbo.U_EFDAGILEXP_EEList GROUP BY xEEID HAVING COUNT(1) > 1);


    --==========================================
    -- Audit Section
    --==========================================
    -- Get data from audit fields table. Add fields here if auditing
    IF OBJECT_ID('U_EFDAGILEXP_AuditFields','U') IS NOT NULL
        DROP TABLE dbo.U_EFDAGILEXP_AuditFields;
    CREATE TABLE dbo.U_EFDAGILEXP_AuditFields (aTableName varchar(30),aFieldName varchar(30));
    INSERT INTO dbo.U_EFDAGILEXP_AuditFields VALUES ('EmpComp','EecEmplStatus');
  

    -- Create audit table based on fields defined above
    IF OBJECT_ID('U_EFDAGILEXP_Audit','U') IS NOT NULL
        DROP TABLE dbo.U_EFDAGILEXP_Audit;
    SELECT 
        audEEID  = audKey1Value
        ,audCOID = audKey2Value
        ,audKey3 = audKey3Value
        ,audTableName
        ,audFieldName
        ,audAction
        ,audDateTime
        ,audOldValue
        ,audNewValue
        ,audTerm = CASE WHEN audTableName = 'EmpComp' AND audFieldName = 'EecEmplStatus' AND ISNULL(audNewValue,'') = 'T' THEN 'Y' ELSE 'N' END
        ,audRowNo = ROW_NUMBER() OVER (PARTITION BY audKey1Value, audKey2Value, audKey3Value, audFieldName ORDER BY audDateTime DESC)
    INTO dbo.U_EFDAGILEXP_Audit
    FROM dbo.vw_AuditData WITH (NOLOCK) 
    JOIN dbo.U_EFDAGILEXP_AuditFields WITH (NOLOCK) 
        ON audTableName = aTableName
        AND audFieldName = aFieldName
    WHERE audDateTime BETWEEN @StartDate AND @EndDate
    AND audAction <> 'DELETE';

    -- Create Index
    CREATE CLUSTERED INDEX CDX_U_EFDAGILEXP_Audit ON dbo.U_EFDAGILEXP_Audit (audEEID,audCOID);

    ----================
    ---- Changes Only
    ----================
    --DELETE FROM dbo.U_EFDAGILEXP_EEList
    --WHERE NOT EXISTS (SELECT 1 FROM dbo.U_EFDAGILEXP_Audit WHERE audEEID = xEEID AND audRowNo = 1);

    --==========================================
    -- Build Driver Tables
    --==========================================
    ---------------------------------
    -- DETAIL RECORD - U_EFDAGILEXP_drvTbl
    ---------------------------------
    IF OBJECT_ID('U_EFDAGILEXP_drvTbl','U') IS NOT NULL
        DROP TABLE dbo.U_EFDAGILEXP_drvTbl;
    SELECT DISTINCT
         drvEEID = xEEID
        ,drvCoID = xCoID
        ,drvDepRecID = CONVERT(varchar(12),'1') --DELETE IF NOT USING DEPENDENT DATA
        ,drvInitialSort = ''
        ,drvSubsort = ''
        -- standard fields above and additional driver fields below
        ,drvSSN = dbo.dsi_fnpadzero(eepSSN,11,0)
        ,drvEmployeeID = dbo.dsi_fnpadzero(EecEmpNo,12,0)
        ,drvNameLast = Ltrim(RTrim(EepNameLast))
        ,drvNameFirst = Ltrim(Rtrim(dbo.dsi_fnRemoveChars('.,/-',EepNameFirst)))
        ,drvNameMiddle = dbo.dsi_fnRemoveChars('.',EepNameMiddle)
        ,drvPreferredName = EepNamePreferred
        ,drvPrefix = EepNamePrefix 
        ,drvSuffix = EepNameSuffix
        ,drvAddressLine1 = EepAddressLine1
        ,drvAddressLine2 = EepAddressLine2
        ,drvAddressCity = EepAddressCity
        ,drvAddressState = EepAddressState
        ,drvAddressZipCode = CASE WHEN LEN(EepAddressZipCode) > 5 THEN  LEFT(EepAddressZipCode, 5) + '-' + RIGHT(EepAddressZipCode, 4) ELSE EepAddressZipCode END
        ,drvAddressCountry = EepAddressCountry
        ,drvTelephone = EepPhoneHomeNumber
        ,drvCellPhone =  efoPhoneNumber
        ,drvEMailAddress = eepAddressEMailAlternate
        ,drvSex = CASE WHEn eepgender IN ('M','F')  THEN eepgender ELSE 'U' END
        ,drvGenderIdentity = CASE WHEn eepgender IN ('M','F')  THEN eepgender ELSE 'O' END
        ,drvMaritalStatus = eepMaritalStatus
        ,drvBirthDate = CONVERT(CHAR(10),EepDateOfBirth, 112)
        ,drvDeathDate = CONVERT(CHAR(10),EepDateDeceased, 112)
        ,drvCountryofOrigin = EepAddressCountry
        ,drvFirstLanguage = EecLanguageCode
        ,drvCompanyCode = CmpCompanyCode
        ,drvJobTitle = dbo.dsi_fnRemoveChars('.,/-',JbcDesc)
        ,drvEmployType = EecEEType
        ,drvEmployStatus = EecEmplStatus 
        ,drvHireDate = CONVERT(CHAR(10),CASE WHEN EecDateOfLastHire <> EecDateOfOriginalHire AND (EecJobChangeReason = '101'OR EecHireSource = 'REHIRE' ) THEN  EecDateOfLastHire Else EecDateOfOriginalHire END, 112)

        ,drvTermDate = CONVERT(CHAR(10),CASE WHEN EecEmplStatus = 'T' THEN EecDateOfTermination END,112)
        ,drvLOAStartDate = CONVERT(CHAR(10),CASE WHEn EecEmplStatus = 'L' THEN  EecEmplStatusStartDate END, 112)
        ,drvLOAThruDate = CONVERT(CHAR(10),CASE WHEn EecEmplStatus = 'A' THEN  EshStatusStopDate END, 112)
        ,drvSalary = FORMAT(EecAnnSalary,'#0.00')
        ,drvWorkEmail = EepAddressEMail
    INTO dbo.U_EFDAGILEXP_drvTbl
    FROM dbo.U_EFDAGILEXP_EEList WITH (NOLOCK)
    JOIN dbo.vw_int_EmpComp WITH (NOLOCK)
        ON EecEEID = xEEID 
        AND EecCoID = xCoID
    JOIN dbo.Company WITH (NOLOCK)
        ON CmpCoID = xCoID
    JOIN dbo.EmpPers WITH (NOLOCK)
        ON EepEEID = xEEID
    JOIN dbo.JobCode WITH (NOLOCK)
        ON JbcJobCode = EecJobCode
    LEFT JOIN dbo.EmpMPhon WITH (NOLOCK)
        ON EfoEEID = xEEID
        AND efoPhoneType = 'CEL'
     LEFT JOIN (
                SELECT EshEEID, EshCOID,  EshEmplStatus, EshStatusStartDate, EshStatusStopDate
                FROM (
                        SELECT EshEEID, EshCOID, EshStatusStartDate, EshStatusStopDate, EshEmplStatus, ROW_NUMBER() OVER (PARTITION BY EshEEID, EshCOID ORDER BY EshStatusStartDate DESC) AS RN
                        FROM dbo.EmpHStat WITH (NOLOCK) 
                        WHERE EshEmplStatus = 'L') AS LOA_Dates 
                WHERE RN = 1) AS EshDates 
        ON EshEEID = xEEID 
        AND EshCOID = xCOID
      LEFT JOIN dbo.U_EFDAGILEXP_Audit WITH (NOLOCK)
        ON audEEID = xEEID
        AND audCOID = xCOID
    WHERE  (EecEmplStatus <> 'T' OR (EecEmplStatus = 'T' AND audTerm = 'Y' AND AudDateTime IS NOT NULL AND AudDateTime BETWEEN @StartDate AND @EndDate))
    

    --==========================================
    -- Set FileName
    --==========================================
    IF (dbo.dsi_fnVariable(@FormatCode,'UseFileName') = 'N')
    BEGIN
        UPDATE dbo.U_dsi_Parameters
            SET ExportFile = CASE WHEN dbo.dsi_fnVariable(@FormatCode,'Testing') = 'Y' THEN 'Test_Filename_' + CONVERT(VARCHAR(8),GETDATE(),112) + '.txt'
                                  WHEN @ExportCode LIKE 'OE%' THEN 'OE_Filename_' + CONVERT(VARCHAR(8),GETDATE(),112) + '.txt'
                                  ELSE 'Filename_' + CONVERT(VARCHAR(8),GETDATE(),112) + '.txt'
                             END
        WHERE FormatCode = @FormatCode;
    END

END;
