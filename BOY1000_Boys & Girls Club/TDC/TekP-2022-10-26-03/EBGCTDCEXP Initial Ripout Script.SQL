SELECT * FROM dbo.U_dsi_Configuration WHERE FormatCode = 'EBGCTDCEXP';
SELECT * FROM dbo.U_dsi_SqlClauses WHERE FormatCode = 'EBGCTDCEXP';
SELECT * FROM dbo.U_dsi_Parameters WHERE FormatCode = 'EBGCTDCEXP';
SELECT ExpFormatCode, ExpExportCode, ExpStartPerControl, ExpEndPerControl,* FROM dbo.AscExp WHERE expFormatCode = 'EBGCTDCEXP';
SELECT * FROM dbo.U_dsi_InterfaceActivityLog WHERE FormatCode = 'EBGCTDCEXP' ORDER BY RunID DESC;

Execute Export
--------------
EXEC dbo.dsi_sp_TestSwitchbox_v2 'EBGCTDCEXP', 'ONDEM_XOE';
EXEC dbo.dsi_sp_TestSwitchbox_v2 'EBGCTDCEXP', 'TEST_XOE';
EXEC dbo.dsi_sp_TestSwitchbox_v2 'EBGCTDCEXP', 'SCH_EBGCTD';

EXEC dbo._dsi_usp_ExportRipOut_v7_4 @FormatCode = 'EBGCTDCEXP', @AllObjects = 'Y', @IsWeb = 'Y'
**********************************************************************************/
BEGIN

    --==========================================
    -- Declare variables
    --==========================================
    DECLARE  @FormatCode        VARCHAR(10)
            ,@ExportCode        VARCHAR(10)
            ,@StartDate         DATETIME
            ,@EndDate           DATETIME
            ,@StartPerControl   VARCHAR(9)
            ,@EndPerControl     VARCHAR(9);

    -- Set FormatCode
    SELECT @FormatCode = 'EBGCTDCEXP';

    -- Declare dates from Parameter file
    SELECT
         @StartPerControl = StartPerControl
        ,@EndPerControl   = EndPerControl
        ,@StartDate       = LEFT(StartPerControl,8)
        ,@EndDate         = DATEADD(S,-1,DATEADD(D,1,LEFT(EndPerControl,8)))
        ,@ExportCode      = ExportCode
    FROM dbo.U_dsi_Parameters WITH (NOLOCK)
    WHERE FormatCode = @FormatCode;

    --==========================================
    -- Clean EE List 
    -- Caution: Careful of cleaning EE List if including paycheck data
    --==========================================

    -- Cleans EE List of terms where EE active in another company (transfer), or active in more than one company
    DELETE FROM dbo.U_EBGCTDCEXP_EEList
    WHERE xCoID <> dbo.dsi_BDM_fn_GetCurrentCOID(xEEID)
    AND xEEID IN (SELECT xEEID FROM dbo.U_EBGCTDCEXP_EEList GROUP BY xEEID HAVING COUNT(1) > 1);


    --==========================================
    -- Audit Section
    --==========================================
    -- Get data from audit fields table. Add fields here if auditing
    IF OBJECT_ID('U_EBGCTDCEXP_AuditFields','U') IS NOT NULL
        DROP TABLE dbo.U_EBGCTDCEXP_AuditFields;
    CREATE TABLE dbo.U_EBGCTDCEXP_AuditFields (aTableName varchar(30),aFieldName varchar(30));

     INSERT INTO dbo.U_EBGCTDCEXP_AuditFields VALUES ('EmpComp','EecDateofTermination');
     INSERT INTO dbo.U_EBGCTDCEXP_AuditFields VALUES ('EmpComp','EecEmplStatus');

    -- Create audit table based on fields defined above
    IF OBJECT_ID('U_EBGCTDCEXP_Audit','U') IS NOT NULL
        DROP TABLE dbo.U_EBGCTDCEXP_Audit;
    SELECT 
        audEEID  = audKey1Value
        ,audKey2 = audKey2Value
        ,audKey3 = audKey3Value
        ,audTableName
        ,audFieldName
        ,audAction
        ,audDateTime
        ,audOldValue
        ,audNewValue
        ,audRowNo = ROW_NUMBER() OVER (PARTITION BY audKey1Value, audKey2Value, audKey3Value, audFieldName ORDER BY audDateTime DESC)
    INTO dbo.U_EBGCTDCEXP_Audit
    FROM dbo.vw_AuditData WITH (NOLOCK) 
    JOIN dbo.U_EBGCTDCEXP_AuditFields WITH (NOLOCK) 
        ON audTableName = aTableName
        AND audFieldName = aFieldName
    WHERE audDateTime BETWEEN @StartDate AND @EndDate
    AND audAction <> 'DELETE';

    -- Create Index
    CREATE CLUSTERED INDEX CDX_U_EBGCTDCEXP_Audit ON dbo.U_EBGCTDCEXP_Audit (audEEID,audKey2);

    --================
    -- Changes Only
    --================
    DELETE FROM dbo.U_EBGCTDCEXP_EEList
    WHERE NOT EXISTS (SELECT 1 FROM dbo.U_EBGCTDCEXP_Audit WHERE audEEID = xEEID AND audRowNo = 1);

    --==========================================
    -- Build Driver Tables
    --==========================================
    ---------------------------------
    -- DETAIL RECORD - U_EBGCTDCEXP_drvTbl
    ---------------------------------
    IF OBJECT_ID('U_EBGCTDCEXP_drvTbl','U') IS NOT NULL
        DROP TABLE dbo.U_EBGCTDCEXP_drvTbl;
    SELECT DISTINCT
         drvEEID = xEEID
        ,drvCoID = xCoID
        ,drvDepRecID = CONVERT(varchar(12),'1') --DELETE IF NOT USING DEPENDENT DATA
        ,drvInitialSort = ''
        ,drvSubsort = ''
        -- standard fields above and additional driver fields below
        ,drvUserID = EecEmpno
        ,drvNameFirst = EepNameFirst
        ,drvNameLast = EepNameLast
        ,drvUsername = EecEmpno
        ,drvApprover = EecSupervisorID
        ,drvManager = EecSupervisorID
        ,drvEmail = EepAddressEMail
        ,drvfOriginalHireDate = EecDateOfLastHire
        ,drvDateofLastHire = EecDateOfLastHire
        ,drvStatus = CASE WHEn  EecEmplStatus <> 'T' then 'Active' else 'Inactive' END
        ,drvtermreason = ''
        ,drvTerminationDate = CASE WHEN EecDateofTermination IS NOT NULL then EecDateOfTermination END
        ,drvDivision = EecOrgLvl1
        ,drvJobTitle = EecJobCode
        ,drvSalaryGrade = JbcSalaryGrade
        ,drvDepartment = EecOrgLvl2
    INTO dbo.U_EBGCTDCEXP_drvTbl
    FROM dbo.U_EBGCTDCEXP_EEList WITH (NOLOCK)
    JOIN dbo.vw_int_EmpComp WITH (NOLOCK)
        ON EecEEID = xEEID 
        AND EecCoID = xCoID
    JOIN dbo.EmpPers WITH (NOLOCK)
        ON EepEEID = xEEID
    JOIN dbo.JobCode WITH (NOLOCK)
        ON JbcJobCode = EecJobCode
     LEFT JOIN (SELECT DISTINCT 
                 OrgCode 
                ,OrgDesc
          FROM dbo.OrgLevel WITH (NOLOCK)
          Where OrgLvl = 1 
          GROUP BY  OrgCode,OrgDesc)OrgCode1
          ON OrgCode1.OrgCode = EecOrgLvl1
     
    LEFT JOIN (SELECT DISTINCT OrgCode,OrgDesc
          FROM dbo.OrgLevel WITH (NOLOCK)
          Where  OrgLvl = 2
          GROUP BY OrgCode,OrgDesc)OrgCode2
          ON OrgCode2.OrgCode = EecOrgLvl2
    Left Join dbo.U_EBGCTDCEXP_Audit
        ON audeeid = xeeid
        and audKey2 = xcoid
       WHERE (EecEmplStatus <> 'T' OR (EecEmplStatus = 'T' AND AudDateTime BETWEEN  @StartDate  AND @EndDate  ))
    ;

    --==========================================
    -- Set FileName
    --==========================================
    IF (dbo.dsi_fnVariable(@FormatCode,'UseFileName') = 'N')
    BEGIN
        UPDATE dbo.U_dsi_Parameters
            SET ExportFile = CASE WHEN dbo.dsi_fnVariable(@FormatCode,'Testing') = 'Y' THEN 'Test_Filename_' + CONVERT(VARCHAR(8),GETDATE(),112) + '.txt'
                                  WHEN @ExportCode LIKE 'OE%' THEN 'OE_Filename_' + CONVERT(VARCHAR(8),GETDATE(),112) + '.txt'
                                  ELSE 'Filename_' + CONVERT(VARCHAR(8),GETDATE(),112) + '.txt'
                             END
        WHERE FormatCode = @FormatCode;
    END

END;
